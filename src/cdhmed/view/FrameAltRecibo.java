    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        String data = jFormattedTextFieldDataRecibo.getText().replace("/", "");
        ArrayList<Medico> listaMedico = Controller.pesquisaMed(jComboBoxMedico.getSelectedItem().toString());
        int id_medico = listaMedico.get(0).getIdMedico();
        Controller.atualizarRecibo(Integer.parseInt(jTextFieldNumRecibo.getText()), jComboBoxProcedimento.getSelectedItem().toString(), data, Double.parseDouble(jTextFieldValor.getText()), id_medico);
        dispose();
    }//GEN-LAST:event_jButtonGravarActionPerformed

