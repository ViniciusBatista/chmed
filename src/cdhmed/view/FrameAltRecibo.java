    public void insereDadosCamposdeTexto() {
        Recibo recibo = Controller.pesquisaReciboNumero(codigoSelecionado);
        Medico med = Controller.pesquisaIdMed(recibo.getIdMedico());
        String data = Controller.formataData(recibo.getData());
        jTextFieldNumRecibo.setText(String.valueOf(recibo.getNum_recibo()));
        jTextFieldValor.setText(String.valueOf(recibo.getValor()));
        jFormattedTextFieldDataRecibo.setText(data);
        inserirDadosnoComboBox();
        jComboBoxMedico.setSelectedItem(med.getNome());
        jComboBoxProcedimento.setSelectedItem(recibo.getProcedimento());
    }

    private void inserirDadosnoComboBox() {
        ArrayList<Medico> listaMed = Controller.pesquisaAllMed();
        ArrayList<String> listaProcedimento = Controller.consultaProcedimento();
        for (int i = 0; i < listaMed.size(); i++) {
            jComboBoxMedico.addItem(listaMed.get(i).getNome());
        }
        for (int i = 0; i < listaProcedimento.size(); i++) {
            jComboBoxProcedimento.addItem(listaProcedimento.get(i));
        }
    }

